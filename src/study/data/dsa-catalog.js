// Convert to detailed format with id + starterCode + defaults
function toDetailed(list) {
  return list.map((p, i) => {
    const fn = (p.fnName || p.n.replace(/[^a-zA-Z0-9]/g, "").replace(/^\w/, c => c.toLowerCase())).slice(0, 40);
    return {
      ...p,
      id: `dsa-${i}`,
      starterCode: p.starterCode || {
        javascript: `function ${fn}(input) {\n  // ${p.i || "your code"}\n  \n}`,
        python: `def ${fn.replace(/([A-Z])/g, '_$1').toLowerCase()}(input):\n    # ${p.i || "your code"}\n    pass`,
        java: `class Solution {\n    // ${p.i || "your code"}\n}`,
        cpp: `class Solution {\npublic:\n    // ${p.i || "your code"}\n};`,
      },
      fnName: p.fnName || fn,
      testCases: p.testCases || [],
      desc: p.desc || `${p.n} (${p.d}) — ${p.i || p.pattern}. Topic: ${p.t}. Companies: ${p.companies}.${p.lc ? ` LeetCode ${p.lc}.` : ""}`,
      examples: p.examples || [],
      constraints: p.constraints || [],
    };
  });
}

