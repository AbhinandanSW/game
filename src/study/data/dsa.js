function ensureDefaults(list) {
  return list.map(p => {
    if (p.starterCode) return p;
    const fn = p.fnName || p.n.replace(/[^a-zA-Z0-9]/g, "").replace(/^\w/, c => c.toLowerCase()) || "solve";
    return {
      ...p,
      desc: p.desc || `${p.n} — ${p.i || "Solve this problem."}\n\nTopic: ${p.t}\nPriority: ${p.p || "P1"}\nDifficulty: ${p.d}`,
      examples: p.examples || [],
      constraints: p.constraints || [],
      starterCode: {
        javascript: `function ${fn}(input) {\n  // your code here\n  \n}`,
        python: `def ${fn.replace(/([A-Z])/g, '_$1').toLowerCase()}(input):\n    # your code here\n    pass`,
        java: `class Solution {\n    // your code here\n}`,
        cpp: `class Solution {\npublic:\n    // your code here\n};`,
      },
      testCases: p.testCases || [],
      fnName: p.fnName || fn,
    };
  });
}

